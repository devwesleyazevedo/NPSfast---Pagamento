function updateform(){
    main_form.forEach(function(mainform_number){
        mainform_number.classList.remove('active');
    })
    main_form[formnumber].classList.add('active');
} 
 
function progress_forward(){
    // step_list.forEach(list => {
        
    //     list.classList.remove('active');
         
    // }); 
    
     
    num.innerHTML = formnumber+1;
    step_list[formnumber].classList.add('active');
}  

function progress_backward(){
    var form_num = formnumber+1;
    step_list[form_num].classList.remove('active');
    num.innerHTML = form_num;
} 
 
var step_num_content=document.querySelectorAll(".step-number-content");

 function contentchange(){
     step_num_content.forEach(function(content){
        content.classList.remove('active'); 
        content.classList.add('d-none');
     }); 
     step_num_content[formnumber].classList.add('active');
 } 
 
 
function validateform(){
    validate=true;
    var validate_inputs=document.querySelectorAll(".main.active input");
    validate_inputs.forEach(function(vaildate_input){
        vaildate_input.classList.remove('warning');
        if(vaildate_input.hasAttribute('require')){
            if(vaildate_input.value.length==0){
                validate=false;
                vaildate_input.classList.add('warning');
            }
        }
    });
    return validate;
    
}








function toggleCampos() {
  var selectElement = document.getElementById("tipoCliente");
  var divJuridica = document.getElementById("juridica");

  if (selectElement.value === "Pessoa Física") {
      // Se a opção Pessoa Física for selecionada, oculte a div "juridica".
      divJuridica.style.display = "none";
  } else if (selectElement.value === "Pessoa Jurídica") {
      // Se a opção Pessoa Jurídica for selecionada, mostre a div "juridica".
      divJuridica.style.display = "flex"; // Ou "inline" ou qualquer outro valor de exibição apropriado.
  }
}






